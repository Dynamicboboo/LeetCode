class Solution {
    public int strStr(String haystack, String needle) {
        int L = haystack.length(), N = needle.length();
        for (int star = 0; star < L-N+1; ++star) {
            if (haystack.substring(star,star+N).equals(needle)){
                return star;
            }
        }
        return -1;
    }
}
