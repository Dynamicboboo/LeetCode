    void reverse(int[] nums, int start) {
        int i = start,j = nums.length-1;
        while (i<j){
            int temp = nums[i];
            nums[i] = nums[j];
            nums[j] = temp;
            i++;
            j--;
        }
    }

    void swap (int[] nums ,int i , int j){
        int temp = nums[i];
        nums[i] = nums[j];
        nums[j] = temp; 
    }
